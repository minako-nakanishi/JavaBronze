class Add {

    public static void main(String[] args){
        System.out.println(10 + 3);
        System.out.println(10 - 3);
        System.out.println(10 * 3);
        // 整数 / 浮動小数
        System.out.println(10 / 3.0); // 3.3333333333333335
        // 整数 / 整数
        System.out.println(10 / 3); // 3
        System.out.println(10 % 3); // 余り

        
    }
}